(function preparePlots() {
  const rng = (seed => () => (seed = (seed * 9301 + 49297) % 233280) / 233280)(7);
  const range = (a, b) => a + Math.floor(rng() * (b - a + 1));

  const plots = RAW_PLOTS.map(p => {
    const m2 = p.area_m2 || range(800, 1200);
    return {
      id: p.id,
      cx: p.cx, cy: p.cy, w: p.w, h: p.h,
      area_m2: m2,
      area: Math.round(m2 / 100 * 10) / 10, // сотки с одной десятичной (9.4)
      status: "free",                        // все свободны
      cadastral: `66:25:0701${range(100, 999)}:${range(10, 99)}`,
      utilities: ["Газ", "Электричество", "Дорога"],
    };
  });

  const village = window.DUBROVO_DATA.village;
  village.freeLots = plots.length;
  village.reservedLots = 0;
  village.soldLots = 0;
  // Площадь 1 очереди — сумма участков + дороги/общие зоны (≈+40%)
  const totalM2 = plots.reduce((s, p) => s + p.area_m2, 0);
  village.area = Math.round(totalM2 * 1.4 / 100) / 100; // в гектарах ~

  window.DUBROVO_DATA.plots = plots;
})();
